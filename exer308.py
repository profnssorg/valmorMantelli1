# Declaração de variável

x = 0

# Atribuição de valor a variavel

x = int(input("Diga a quantidade de metros que deseja converter em milímetros: "))

#Processamento

conv = x * 1000

# Saída

print("%d metros corresponde a %d milímetros." %(x, conv))

